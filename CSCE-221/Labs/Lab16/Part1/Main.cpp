#include <iostream>
#include "Color.h"
using namespace std;

//////////////////////////////////////////////////////////////////////////////
//                                  main()                                  //
//////////////////////////////////////////////////////////////////////////////
int main()
{
    Color color(80,0,0);
    cout << "Color color : " << color.to_str() << endl;

    
    Color color2(80,0,0);
    cout << "Color color2 : " << color2.to_str() << endl;
    cout << "color == color2 : " << ((color==color2)? "True" : "False") << endl;

    Color color3(255,128,0);
    cout << "Color color3 : " << color3.to_str() << endl;
    cout << "color == color3 : " << ((color==color3)? "True" : "False") << endl;
    
}

